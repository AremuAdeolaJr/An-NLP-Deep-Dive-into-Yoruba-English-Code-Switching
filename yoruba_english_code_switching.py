# CELL 3 — Semantic space visualisation
# ============================================================
# Prerequisites : Cell 1 has been run this session
# Reads         : candidates.csv
# Writes        : semantic_space.png
#
# Produces a two-panel figure:
#   Left  -- UMAP scatter of all sentences coloured by theme.
#            Sentences that are semantically similar cluster
#            together. Well-separated clusters mean themes are
#            genuinely topically distinct.
#   Right -- Intra-theme mean cosine distance bar chart.
#            Higher bar = more diverse sentences within that
#            theme. A very low bar signals near-duplicates.
# ============================================================

def visualise_semantic_space():
    # Read from disk -- no dependency on Cell 2 being in memory
    try:
        candidates_df = pd.read_csv(CANDIDATES_FILE)
    except FileNotFoundError:
        print("candidates.csv not found. Run Cell 2 first.")
        return

    sentences = candidates_df["sentence"].tolist()
    if len(sentences) < 4:
        print("Need at least 4 sentences to plot. Run Cell 2 first.")
        return

    themes = (
        candidates_df["theme"].tolist()
        if "theme" in candidates_df.columns
        else ["unknown"] * len(sentences)
    )

    print("Encoding sentences with multilingual model...")
    model       = get_embedding_model()
    embeddings  = model.encode(sentences, show_progress_bar=True)
    sim_matrix  = cosine_similarity(embeddings)
    dist_matrix = 1.0 - sim_matrix  # cosine distance (0=identical, 1=opposite)

    print("Running UMAP projection to 2D...")
    reducer   = UMAP(n_neighbors=min(10, len(sentences) - 1),
                     min_dist=0.15, random_state=42)
    coords_2d = reducer.fit_transform(embeddings)

    unique_themes = sorted(set(themes))

    # Per-theme mean pairwise cosine distance
    theme_mean_dist = {}
    for t in unique_themes:
        idx = [i for i, th in enumerate(themes) if th == t]
        if len(idx) >= 2:
            sub   = dist_matrix[np.ix_(idx, idx)]
            upper = sub[np.triu_indices(len(idx), k=1)]
            theme_mean_dist[t] = float(upper.mean())

    fig = plt.figure(figsize=(20, 8))
    fig.patch.set_facecolor("#0F0F1A")
    gs  = gridspec.GridSpec(1, 2, width_ratios=[3, 2], figure=fig)
    ax_scatter = fig.add_subplot(gs[0])
    ax_bar     = fig.add_subplot(gs[1])
    dark_ax(ax_scatter)
    dark_ax(ax_bar)

    # Left panel: UMAP scatter coloured by theme
    for t in unique_themes:
        idx = np.array([i for i, th in enumerate(themes) if th == t])
        ax_scatter.scatter(
            coords_2d[idx, 0], coords_2d[idx, 1],
            c=THEME_PALETTE.get(t, "#888780"), s=90, zorder=3,
            label=t.replace("_", " "), edgecolors="#0F0F1A", linewidths=0.8,
        )
    for i, sentence in enumerate(sentences):
        short = " ".join(sentence.split()[:4]) + "..."
        ax_scatter.annotate(short,
            xy=(coords_2d[i, 0], coords_2d[i, 1]),
            xytext=(5, 5), textcoords="offset points",
            fontsize=7, color="white", alpha=0.65, zorder=5)
    ax_scatter.legend(fontsize=9, facecolor="#1A1A2E", labelcolor="white",
                      edgecolor="#444", loc="upper left", framealpha=0.9)
    ax_scatter.set_title(
        f"UMAP semantic space -- {len(sentences)} sentences, {len(unique_themes)} themes",
        fontsize=12, fontweight="bold", color="white", pad=12)
    ax_scatter.set_xlabel("UMAP dimension 1", fontsize=10)
    ax_scatter.set_ylabel("UMAP dimension 2", fontsize=10)

    # Right panel: intra-theme cosine distance bar chart
    if theme_mean_dist:
        bar_themes = list(theme_mean_dist.keys())
        bar_values = [theme_mean_dist[t] for t in bar_themes]
        bar_colors = [THEME_PALETTE.get(t, "#888780") for t in bar_themes]
        bars = ax_bar.barh(
            [t.replace("_", " ") for t in bar_themes], bar_values,
            color=bar_colors, edgecolor="#0F0F1A", linewidth=1.2)
        for bar, val in zip(bars, bar_values):
            ax_bar.text(val + 0.003, bar.get_y() + bar.get_height() / 2,
                        f"{val:.3f}", va="center", ha="left",
                        color="white", fontsize=9)
        all_upper   = dist_matrix[np.triu_indices(len(sentences), k=1)]
        corpus_mean = float(all_upper.mean())
        ax_bar.axvline(corpus_mean, color="#888780", linewidth=1.0,
                       linestyle="--", alpha=0.7,
                       label=f"Corpus mean: {corpus_mean:.3f}")
        ax_bar.legend(fontsize=8, facecolor="#1A1A2E", labelcolor="white",
                      edgecolor="#444", framealpha=0.9)
        ax_bar.set_title("Intra-theme diversity\n(mean pairwise cosine distance)",
                         fontsize=12, fontweight="bold", color="white", pad=12)
        ax_bar.set_xlabel("Mean cosine distance (higher = more diverse)", fontsize=10)
        ax_bar.set_xlim(0, max(bar_values) * 1.25)
        ax_bar.text(0.98, 0.03,
                    "Higher = more spread out\nLower = more similar",
                    transform=ax_bar.transAxes, fontsize=8, color="#aaaaaa",
                    ha="right", va="bottom",
                    bbox=dict(boxstyle="round,pad=0.3", facecolor="#2A2A3E",
                              edgecolor="#555", alpha=0.85))

        print("\nIntra-theme mean cosine distances:")
        for t, d in sorted(theme_mean_dist.items(), key=lambda x: x[1]):
            bar_str = "█" * int(d * 80)
            print(f"  {t:<20} {d:.4f}  {bar_str}")

    fig.suptitle("Semantic space analysis -- Yoruba-English code-switched candidates",
                 fontsize=14, fontweight="bold", color="white", y=1.01)
    plt.tight_layout()
    plt.savefig("semantic_space.png", dpi=150, bbox_inches="tight",
                facecolor=fig.get_facecolor())
    plt.show()
    print("\nSaved to 'semantic_space.png'.")
    print("Next step: run Cell 4 (human quality review).")

# ── Run ──────────────────────────────────────────────────────
