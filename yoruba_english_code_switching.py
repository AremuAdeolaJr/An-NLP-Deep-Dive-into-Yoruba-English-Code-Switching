# ============================================================
# CELL 5 — Combined NER + POS annotation
# ============================================================
# Prerequisites : Cell 1 has been run this session
# Reads         : approved_sentences.csv
# Writes        : ner_output.csv, pos_output.csv
#
# Sends all approved sentences to GPT-4 in batches of 10.
# Each batch is a SINGLE API call that returns both NER entity
# labels and POS tags simultaneously -- halving the number of
# API calls and cutting annotation cost by ~50% compared to
# running NER and POS in separate passes.
#
# If ner_output.csv already exists from a previous run, this
# cell skips re-annotation and just loads the existing files.
# ============================================================

def run_annotation():
    # Skip if already done
    if os.path.exists(NER_FILE) and os.path.exists(POS_FILE):
        df_ner = pd.read_csv(NER_FILE)
        df_pos = pd.read_csv(POS_FILE)
        print(f"Annotation already complete.")
        print(f"  NER: {len(df_ner)} entity records in '{NER_FILE}'")
        print(f"  POS: {len(df_pos)} token records in '{POS_FILE}'")
        print("Next step: run Cell 6 (NER verification).")
        return df_ner, df_pos

    try:
        approved_df = pd.read_csv(APPROVED_FILE)
    except FileNotFoundError:
        print("approved_sentences.csv not found. Run Cell 4 first.")
        return None, None

    if approved_df.empty:
        print("No approved sentences found. Complete Cell 4 first.")
        return None, None

    print(f"Annotating {len(approved_df)} sentences (NER + POS, single API pass)...")
    ner_records, pos_records = [], []
    batch_size   = 10
    sentence_list = approved_df["sentence"].tolist()
    batches = [sentence_list[i:i + batch_size]
               for i in range(0, len(sentence_list), batch_size)]

    for batch in tqdm(batches, desc="Annotating batches"):
        formatted = "\n".join(f"- {s}" for s in batch)
        messages  = annotation_prompt.format_messages(sentences=formatted)
        output    = call_llm(messages)
        if output is None:
            continue
        try:
            data = parse_json_output(output)
            for item in data:
                sent = item["sentence"]
                for ent in item.get("entities", []):
                    ner_records.append({"sentence": sent, "word": ent["word"],
                                        "entity_label": ent["label"]})
                for tag in item.get("pos_tags", []):
                    pos_records.append({"sentence": sent, "word": tag["word"],
                                        "pos_tag": tag["pos"]})
        except Exception as e:
            print(f"  Parse error on batch: {e}")
            time.sleep(2)

    df_ner = pd.DataFrame(ner_records)
    df_pos = pd.DataFrame(pos_records)
    df_ner.to_csv(NER_FILE, index=False)
    df_pos.to_csv(POS_FILE, index=False)
    print(f"\nDone.")
    print(f"  NER: {len(df_ner)} entity records saved to '{NER_FILE}'")
    print(f"  POS: {len(df_pos)} token records saved to '{POS_FILE}'")
    print("Next step: run Cell 6 (NER verification).")
    return df_ner, df_pos

# ── Run ──────────────────────────────────────────────────────
df_ner, df_pos = run_annotation()

