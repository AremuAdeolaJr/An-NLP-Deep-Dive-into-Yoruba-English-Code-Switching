import ipywidgets as widgets
from IPython.display import display

VALID_LABELS = ["PERSON", "LOCATION", "ORGANIZATION"]

def launch_ner_verification_ui():

    try:
        df_ner = pd.read_csv(NER_FILE)
    except FileNotFoundError:
        print("ner_output.csv not found. Run Cell 5 first.")
        return None

    try:
        verified_df  = pd.read_csv(NER_VERIFIED_FILE)
        already_done = set(zip(verified_df["sentence"], verified_df["word"]))
        print(f"Loaded {len(verified_df)} previously verified entities.")
    except FileNotFoundError:
        verified_df  = pd.DataFrame(
            columns=["sentence", "word", "entity_label", "gpt_label", "status"]
        )
        already_done = set()

    # Build the review queue -- skip any pair already verified in a prior session
    pairs = [
        (row["sentence"], row["word"], row["entity_label"])
        for _, row in df_ner.iterrows()
        if (row["sentence"], row["word"]) not in already_done
    ]

    if not pairs:
        print("All NER entities already verified.")
        df_v = pd.read_csv(NER_VERIFIED_FILE)
        _print_ner_metrics(df_v)
        _plot_ner_distribution(df_v)
        return df_v

    state = {"idx": 0, "pairs": pairs, "verified_df": verified_df}

    # ── Widget layout ─────────────────────────────────────────────────────────
    title = widgets.HTML(
        "<h3 style='margin:0 0 4px 0; color:#e0e0e0'>NER Label Verification</h3>"
        "<p style='margin:0 0 12px 0; color:#aaaaaa; font-size:13px'>"
        "The <b>bracketed word</b> is the entity GPT-4 predicted. "
        "Confirm the label, correct it, or mark it as not an entity at all.</p>"
    )

    sentence_display = widgets.HTML()
    gpt_label_display = widgets.HTML()
    progress_display  = widgets.HTML()

    decision_toggle = widgets.ToggleButtons(
        options=["Correct", "Wrong label", "Not an entity"],
        value="Correct",
        button_style="",
        style={"button_width": "140px"},
    )

    correction_dropdown = widgets.Dropdown(
        options=VALID_LABELS,
        description="Correct label:",
        style={"description_width": "100px"},
        layout=widgets.Layout(width="280px"),
    )
    # Show the correction dropdown only when "Wrong label" is selected
    correction_dropdown.layout.visibility = "hidden"

    def toggle_correction(change):
        correction_dropdown.layout.visibility = (
            "visible" if change["new"] == "Wrong label" else "hidden"
        )
    decision_toggle.observe(toggle_correction, names="value")

    submit_btn = widgets.Button(
        description="Submit",
        button_style="primary",
        layout=widgets.Layout(width="120px", height="40px"),
    )

    output_area = widgets.Output()

    def refresh():
        """Update displayed values to show the current entity in the queue."""
        if state["idx"] >= len(state["pairs"]):
            sentence_display.value = (
                "<div style='padding:12px; background:#1A3A1A; border-radius:6px; "
                "color:#1D9E75; font-size:15px; font-weight:bold'>"
                "All entities verified!</div>"
            )
