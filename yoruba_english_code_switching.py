def generate_candidates():
    df = load_or_create(CANDIDATES_FILE, ["sentence", "theme", "status"])

    if len(df) >= CANDIDATES_TARGET:
        print(f"Generation already complete: {CANDIDATES_TARGET} sentences exist.")
        return df

    print(f"Generating {SENTENCES_PER_THEME} sentences per theme "
          f"({len(THEMES)} themes = {CANDIDATES_TARGET} total)...\n")

    for theme in THEMES:
        name       = theme["name"]
        directions = theme["directions"]
        already    = int((df["theme"] == name).sum()) if "theme" in df.columns else 0

        if already >= SENTENCES_PER_THEME:
            print(f"  [{name}] already complete ({already} sentences). Skipping.")
            continue

        needed   = SENTENCES_PER_THEME - already
        print(f"  [{name}] requesting {needed} sentences from GPT-4...")
        messages = generation_prompt.format_messages(
            num_examples=needed, theme=name, directions=directions
        )
        output = call_llm(messages)
        if output is None:
            print(f"  [{name}] API call failed. Skipping theme.")
            continue

        try:
            data = parse_json_output(output)
        except Exception as e:
            print(f"  [{name}] Parse error: {e}. Skipping theme.")
            continue

        rows = [
            {"sentence": item.get("sentence", "").strip(),
             "theme": name, "status": "pending"}
            for item in data if item.get("sentence", "").strip()
        ][:needed]  # trim to exactly what was requested

        if rows:
            df = pd.concat([df, pd.DataFrame(rows)], ignore_index=True)
            df.to_csv(CANDIDATES_FILE, index=False)
            print(f"  [{name}] saved {len(rows)} sentences.")
        else:
            print(f"  [{name}] no valid sentences returned by GPT-4.")

    print(f"\nTotal in '{CANDIDATES_FILE}': {len(df)}")
    print("Next step: run Cell 3 (semantic space visualisation).")
    return df

