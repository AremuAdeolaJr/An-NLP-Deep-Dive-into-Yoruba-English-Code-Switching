#
# If langid_output.csv already exists, this cell loads it and
# skips re-tagging.
#
# NumPy 2.x compatibility note:
# FastText's internal predict() method uses np.array(probs, copy=False),
# which raises a ValueError in NumPy 2.x because the semantics of copy=False
# changed -- it now means "raise an error if a copy is unavoidable" rather
# than "avoid a copy if you can". The patch below replaces np.array inside
# the fasttext module with a wrapper that silently removes the copy=False
# argument before forwarding the call, restoring the NumPy 1.x behaviour
# without downgrading or reinstalling anything.
# ============================================================

def _patch_fasttext_for_numpy2():
    """
    Apply a one-time monkey-patch so FastText works with NumPy 2.x.

    FastText calls np.array(probs, copy=False) inside predict(). In NumPy 2.x
    this raises: "ValueError: Unable to avoid copy while creating an array as
    requested." The fix is to replace np.array inside the fasttext.FastText
    module with a thin wrapper that strips the copy keyword before forwarding
    to the real np.array. This has no effect on NumPy 1.x runtimes.
    """
    import numpy as _np
    import fasttext.FastText as _ft_module

    _real_array = _np.array

    def _patched_array(obj, *args, **kwargs):
        # Remove the copy keyword that NumPy 2.x no longer accepts with False
        kwargs.pop("copy", None)
        return _real_array(obj, *args, **kwargs)

    # Inject the patched version into the fasttext module's numpy namespace
    _ft_module.np.array = _patched_array

_patch_fasttext_for_numpy2()


def run_language_identification():
    # Skip if already done
    if os.path.exists(LANGID_FILE):
        df_langid = pd.read_csv(LANGID_FILE)
        print(f"Language ID already complete: {len(df_langid)} token records in '{LANGID_FILE}'.")
        print("Next step: run Cell 8 (final visualisation).")
        return df_langid

    try:
        approved_df = pd.read_csv(APPROVED_FILE)
    except FileNotFoundError:
        print("approved_sentences.csv not found. Run Cell 4 first.")
        return None

    # Auto-download the FastText model if it is missing.
    # This is the fix for: ValueError: lid.176.bin cannot be opened!
    ensure_fasttext()

    print(f"Tagging {len(approved_df)} sentences with FastText language ID...")
    fasttext.FastText.eprint = lambda *a, **k: None   # suppress verbose output
    model   = fasttext.load_model(FASTTEXT_PATH)
    records = []

    for _, row in tqdm(approved_df.iterrows(), total=len(approved_df),
                       desc="Tagging tokens"):
        sentence = row["sentence"]
        for word in sentence.split():
            labels, probs = model.predict(word, k=1)
            lang          = labels[0].replace("__label__", "")  # e.g. "yo", "en"
            # Collapse everything that is not Yoruba or English into 'other'
            # so visualisations remain clean and interpretable
            simplified    = lang if lang in ("yo", "en") else "other"
            records.append({
                "sentence":   sentence,
                "word":       word,
                "lang_raw":   lang,        # full FastText prediction preserved
                "lang":       simplified,  # yo / en / other
                "confidence": round(float(probs[0]), 3),
            })

    df_langid = pd.DataFrame(records)
    df_langid.to_csv(LANGID_FILE, index=False)
    print(f"\nSaved {len(df_langid)} token records to '{LANGID_FILE}'.")
    print("Next step: run Cell 8 (final visualisation).")
    return df_langid

# ── Run ──────────────────────────────────────────────────────
